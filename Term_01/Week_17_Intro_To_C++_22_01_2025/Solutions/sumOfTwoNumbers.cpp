#include<iostream>
using namespace std;
int main()
{
    /// Създаваме две променливи от тип int (т.е. цели числа) с имена а и b
    int a, b;

    /// Въвеждаме от конзолата стойности (чрез оператора cin>>) на двете променливи
    cin>>a>>b;

    /// Извеждаме на конзолата сбора на двете променливи
    cout<< a + b;

    return 0;
}